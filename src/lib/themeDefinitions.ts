export const PRESET_THEME_MAP = Object.fromEntries(
  PRESET_THEMES.map((theme) => [theme.id, theme])
) as Record<(typeof PRESET_THEME_IDS)[number], PresetTheme>

export function getPresetTheme(themeId: string): PresetTheme | undefined {
  if (!PRESET_THEME_IDS.includes(themeId as (typeof PRESET_THEME_IDS)[number])) {
    return undefined
  }

  return PRESET_THEME_MAP[themeId as (typeof PRESET_THEME_IDS)[number]]
}

export function resolveThemeDefinition(themeId: string, customThemes: CustomTheme[]) {
  return (
    customThemes.find((theme) => theme.id === themeId) ||
    getPresetTheme(themeId) ||
    PRESET_THEME_MAP.default
  )
}
